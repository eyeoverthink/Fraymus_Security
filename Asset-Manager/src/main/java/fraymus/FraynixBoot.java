    private static boolean supportsDirectRun(BuilderDescriptor builder) {
        if (builder == null) {
            return false;
        }
        try {
            Class<?> builderClass = Class.forName("fraymus.os." + builder.className);
            builderClass.getMethod("main", String[].class);
            return true;
        } catch (ReflectiveOperationException e) {
            return false;
        }
    }

    private static BuilderDescriptor findBuilderDescriptor(String name) {
        if (name == null || name.isBlank()) {
            return null;
        }

        String normalized = name.trim().toLowerCase();
        for (BuilderDescriptor builder : BUILDERS) {
            String classKey = builder.className.toLowerCase();
            if (normalized.equals(builder.key)
                    || normalized.equals(classKey)
                    || normalized.equals(classKey.replace("builder", ""))) {
                return builder;
            }
        }
        return null;
    }

